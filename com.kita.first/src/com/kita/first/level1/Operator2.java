package com.kita.first.level1;

public class Operator2 {
	public static void main(String[] args) {
		int n1 = 3;
		System.out.println(n1++); //순서대로 n1값을 출력 후 1씩 증가
		System.out.println(n1);
		System.out.println(++n1); //순서대로 증가된 값을 출력 
		System.out.println(n1);
		System.out.println(n1--);
		System.out.println(n1);
		System.out.println(--n1);
	}

}
